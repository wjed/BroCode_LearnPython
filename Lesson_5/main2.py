x = 3.14
y = -4
z = 5

result = round(x)
print(result)

absolute_value = abs(y)
print(absolute_value)

power = pow(z, 3)
print(f"When raising to the power of {z} to 3, we get the {power}")

maximum = max(x, y, z)
print(f"The maximum value between {x}, {y}, and {z} is {maximum}")

minimum = min(x, y, z)
print(f"The minimum value between {x}, {y}, and {z} is {minimum}")
